
    // Prefix aur Postfix Operators in JavaScript

    // JavaScript mein ++ (increment) aur -- (decrement) operators hoti hain jo value ko badhate ya kam karte hain.

    // 1. Prefix (++) aur (--)

    // Jab operator variable ke pehle hota hai, toh pehle value badhayi ya kam ki jati hai, aur phir wo value use hoti hai.

    // Increment Example (++):

    let a = 5;
    console.log(++a); // Output: 6, pehle increment hota hai aur phir value return hoti hai

    // Decrement Example (--):

    let b = 5;
    console.log(--b); // Output: 4, pehle decrement hota hai aur phir value return hoti hai


    // 2. Postfix (++) aur (--)

    // Jab operator variable ke baad hota hai, toh pehle value ko use kiya jata hai, aur phir wo badhayi ya kam ki jati hai.


    // Increment Example (++):
    let A = 5;
    console.log(A++); // Output: 5, pehle value print hoti hai, phir increment hota hai
    console.log(A);   // Output: 6, ab value increment ho gayi hai

    // Decrement Example (--):

    let B = 5;
    console.log(B--); // Output: 5, pehle value print hoti hai, phir decrement hota hai
    console.log(B);   // Output: 4, ab value decrement ho gayi hai



    /**********************************Summary********************************************/
    // Prefix (++): Value pehle increment ya decrement hoti hai, phir use hoti hai.
    // Postfix (++): Pehle original value use hoti hai, phir value increment ya decrement hoti hai. 

